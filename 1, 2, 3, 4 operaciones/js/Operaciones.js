// Suma

function sumarParametro(numeroUno, numeroDos){
    let numUno = numeroUno
    let numDos =  numeroDos
    return parseInt(numUno)+parseInt(numDos);
}

const sumarExpresion = function(anumeroUno, anumeroDos){
    let numUno = anumeroUno 
    let numDos = anumeroDos 
    return parseInt(numUno)+parseInt(numDos);
}

